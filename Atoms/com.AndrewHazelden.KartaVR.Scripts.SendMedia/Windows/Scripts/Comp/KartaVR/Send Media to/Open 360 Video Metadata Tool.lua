Overview:

The Open 360 Video Metadata Tool script is a module from [KartaVR](http://www.andrewhazelden.com/blog/downloads/kartavr/) that will launch the YouTube 360 / Facebook 360 spatial media metadata embedding program. 

How to use the Script:

Step 1. Start Fusion and open a new comp. Then run the "Script > KartaVR > Send Media to > Open 360 Video Metadata Tool" menu item to launch the YouTube 360 metadata program.

Installing the Metadata Tool:

You can use the Reactor package "Bin/Spatial Media Metadata Injector" to add this tool to your system.


Downloading the Metadata Tool Manually:

You could also download and install the tool manually. You would need to edit the "viewerProgram" variable in the "spatialMediaMetadataTool()" function below.

To learn more about YouTube 360 degree video metadata and uploading the content to YouTube, check out this Youtube support page:
https://support.google.com/youtube/answer/6178631 

or go to the Google Spatial Media GitHub site:
https://github.com/google/spatial-media)

On Mac OS X the metadata tool has to be copied to:
/Applications/Spatial Media Metadata Injector.app

On Windows the metadata tool has to be copied to:
C:\Program Files\Spatial Media Metadata Injector.exe

